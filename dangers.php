        <h2>Heat Stroke</h2>
        <h3>How It Happens:</h3>
        <ul>
            <li>Parent is in a rush and due to the baby being asleep forgets their child.</li>
            <li>Parent isn&rsquo;t aware of the risks of leaving their child in the car and leaves their child there while they go shop or run errands.</li>
        </ul>
        <h3>Facts:</h3>
        <ul>
            <li>About 87% of children who have perished from vehicular heat stroke are age 3 and younger.</li>
            <li>About 54% of vehicular heat stroke deaths involve children age one and under.</li>
            <li>The average number of U.S. child vehicular heat stroke is 37 a year. That&rsquo;s about one every nine days.</li>
            <li>In the first 10 minutes the temperature of a car increases by about 80%!</li>
            <li>There are cases where children have died in cars while the outside temperature was as low as 60 degrees Fahrenheit.</li>
            <li>When a human&rsquo;s body temp reaches 104 degrees Fahrenheit internal organs have a tendency to shut down. Pretty safe to say you kind of need those working.</li>
            <li>At 107 degrees Fahrenheit children die.</li>
            <li>With an outside temperature as low as 60 degrees your car can still heat up to well over 100.</li>
        </ul>
        <h3>Symptoms of Heat Stroke:</h3>
        <ul>
            <li>Hot, red, and dry or moist skin.</li>
            <li>No sweating.</li>
            <li>A slow weak pulse or a rapid strong pulse.</li>
            <li>A throbbing headache.</li>
            <li>Dizziness.</li>
            <li>Nausea.</li>
            <li>Confusion.</li>
            <li>Acting strangely or being grouchy.</li>
        </ul>
        <h3>Prevention Tips:</h3>
        <ul>
            <li>Position your child&rsquo;s car seat so that it isn&rsquo;t directly behind your drivers&rsquo; seat.</li>
            <li>By placing the car seat on the passenger side it allows parents to see their child easier.</li>
            <li>Put something of importance in your back seat before you start driving. That way you are forced to turn around and see your child.</li>
            <li>You can also put something in the back to keep your child from falling asleep like a toy or a diaper bag. If they fall asleep they can't give you auditory clues.</li>
            <li>Try and get into the habit of looking in your back seats before you lock your car.</li>
        </ul>
