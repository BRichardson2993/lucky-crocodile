        <h2>Vehicle Accidentally Getting Set In Motion</h2>
        <h3>How It Happens:</h3>
        <ul>
            <li>Parent leaves keys where child can reach them. Child then proceeds to get in car and set it in motion.</li>
        </ul>
        <h3>Facts:</h3>
        <ul>
            <li>Of all nontraffic fatalities involving children between 2006 and 2010 vehicle in motion made up 5% of all fatalities.</li>
        </ul>
        <h3>Prevention Tips:</h3>
        <ul>
            <li>Always make sure your car is locked and keep your keys out of reach from children.</li>
        </ul>
        <h2>Power Windows</h2>
        <h3>How it Happens:</h3>
        <ul>
            <li>Kid is hanging out the window and accidentally rolls it up by hitting the button.</li>
        </ul>
        <h3>Facts:</h3>
        <ul>
            <li>Power windows in vehicles have injured or killed thousands of children.</li>
            <li>The amount of upward force a power window exerts up is between 30 and 80 pounds. It only takes 22 pounds of force to injure and potentially suffocate an infant.</li>
        </ul>
        <h3>Prevention Tips:</h3>
        <ul>
            <li>Most cars nowadays have the ability to lock window functionality so make sure your windows are always locked.</li>
            <li>Always make sure your children are properly secured in their set while driving.</li>
        </ul>

