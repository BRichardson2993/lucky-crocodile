        <h2>Choking</h2>
        <h3>How It Happens:</h3>
        <ul>
            <li>Small objects (such as a pencil, loose change, seeds, etc.) get left or forgotten in car only to have baby find them and stick them in his/her mouth.</li>
        </ul>
        <h3>Facts:</h3>
        <ul>
            <li>Choking is the fourth leading cause of unintentional death in children under the age of five.</li>
            <li>Children younger than five are at the greatest risk of suffering a choking related injury or death.</li>
            <li>Just about any small item you can think of is a choking hazard.</li>
            <li>The most common cause of choking that is nonfatal in young children is food.</li>
            <li>At least one child dies from choking on food every five days in the U.S., and more than 10,000 children are taken to a hospital emergency room each year for injuries caused by food choking.</li>
        </ul>
        <h3>Symptoms of Choking:</h3>
        <ul>
            <li>Struggling to breathe (gasping).</li>
            <li>Coughing.</li>
            <li>Gaggging.</li>
            <li>Bluish lips or skin.</li>
            <li>High Pitched voice.</li>
        </ul>
        <h3>Prevention Tips:</h3>
        <ul>
            <li>You should never leave a small child unattended while they are eating. Direct supervision is needed.</li>
            <li>Children shouldn&rsquo;t eat while walking, riding in a car or playing.</li>
            <li>Cut your child&rsquo;s food into small pieces, removing seeds and pits.</li>
            <li>Cook vegetables to soften them.</li>
            <li>Chew food properly and model some safe eating habits for your kids.</li>
            <li>Make sure your children have a calm, unhurried meal.</li>
            <li>Clean the inside of your car thoroughly before hand if you are going to have a child sit in it.</li>
        </ul>
        <h2>Front-overs/Back-overs</h2>
        <h3>How It Happens:</h3>
        <ul>
            <li>A frontover is when a driver doesn&rsquo;t see a child in front of the vehicle and goes forward.</li>
            <li>A Backover is when a driver doesn&rsquo;t see a child behind the vehicle and backs up.</li>
        </ul>
        <h3>Facts:</h3>
        <ul>
            <li>Of all nontraffic fatalities involving children under 15 years old from 2006 to 2010 30% of those were from Frontovers while 34% were from Backovers.</li>
            <li>Frontovers and Backovers can happen in any vehicle because all vehicles have a blind zone which is the area in front of a vehicle where you can&rsquo;t see from the driver&rsquo;s seat.</li>
            <li>The predominant age of backover victims is one year old.</li>
            <li>Over 60% of back up incidents involved larger size vehicle such as a truck, van, or SUV.</li>
            <li>Sadly in a little over 70% of these incidents, a close relative or parent are usually the ones behind the wheel.</li>
        </ul>
        <h3>Prevention Tips:</h3>
        <ul>
            <li>Completely walk around and behind your vehicle before moving it.</li>
            <li>You can always measure the size of your blind zone in the front and back of your vehicle. Most drivers cannot see a young child six to eight feet in front or back of their vehicle.</li>
            <li>Teach your kids that there is always a chance for a parked car to move and just because they can see the car doesn&rsquo;t mean the car can see them.</li>
            <li>Keep all sports equipment and toys out of the driveway.</li>
            <li>Know where your kids are before you go anywhere.</li>
        </ul>
