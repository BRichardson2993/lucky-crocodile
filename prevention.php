        <h1>Prevention and Precautions</h1>
        <p>You know the dangers. How do you prevent tragedy?</p>
        <h3>INSIDE THE VEHICLE</h3>

        <h4>Heat Stroke/Hypothermia:</h4>
        <p>Position your child&rsquo;s car seat on the passenger side to see your child easier.</p>
        <p>Stage you car with cues to help remember your child:</p>
        <ul>
        <li>Keep a stuffed animal in the car seat at all times. When you buckle baby in, place it on the dash or in the passenger seat.</li>
        <li>Keep important items you will need in the backseat with baby.</li>
        <li> Keep your vehicle stocked with toys &lpar;not small enough to be choked on&rpar; to keep your child awake and giving auditory prompts of their presence.</li>
        </ul>
        <p>Many devices and apps are now available to help you remember your child.</p>

        <h4>Choking:</h4>
        <p>Clean your vehicle regularly &ndash; especially of small objects such as trinkets, toys, or stray food items that may have fallen into hidden spaces; be sure to look under seats and in storage areas.</p>
        <p>You should never leave a small child unattended while they are eating.</p>
        <p>Children should never eat in the car.</p>
        <p>If your child must eat in your vehicle, remain parked and never leave them unattended.</p>
        <p>Feed your child at home before or after your errands.</p>
        <p>Keep your vehicle clean and free of loose small items.</p>

        <h4>Front-overs/Back-overs:</h4>
        <p>Average blindzone: 15-25 feet &lpar;shorter drivers have a greater blindzone&rpar;</p>
        <p>Teach your children that any vehicle can move</p>
        <p>Keep toys out of the driveway</p>
        <p>Know where your children are before moving a vehicle</p>

        <h4>Vehicle accidentally set in motion:</h4>
        <p>Shut the vehicle off and lock it when you leave it</p>
        <p>Keep your car locked when not in use</p>
        <p>Keep keys out of reach of children</p>

        <h4>Power Windows:</h4>
        <p>When children are riding, engage the child safety lock</p>
        <p>Shut the vehicle off and lock it when you leave it</p>
        <p>Teach children that power windows are dangerous</p>

        <h4>Trunk Entrapment</h4>

        <h4>Abduction</h4>

        <h4>Poison</h4>

        <h3>OUTSIDE THE VEHICLE</h3>
        <p>Always bring your child with you when you exit the vehicle. No amount of time is safe to leave your child unattended, even for &ldquo;just a minute&rdquo;. It is illegal to do so in most states.</p>
        <p>Be sure that your vehicle&rsquo;s doors and trunk remain closed and locked, even when parked at home. Children love to play hide-and-seek and other fun games. A vehicle may seem to them an ideal hiding spot, a stagecoach, a castle, or many other things. A vehicle can quickly become dangerous, as the child can easily become trapped within it. Instruct children that the vehicle is not a safe place to play.</p>
        <p>Familiarize yourself with life-saving techniques such as the Heimlich Maneuver, child cardiopulmonary resuscitation &lpar;CPR&rpar;, and other first-aid measures, and call 911 for emergencies.</p>
        <p>Make caretakers aware of the dangers and solutions, and always have a plan of action in case of emergency.</p>
        <p>Greatest of all: YOU KNOW YOUR CHILD. If they are exhibiting abnormal behavior or exhibiting any of the signs of illness or injury, call the doctor or emergency services immediately!</p>

